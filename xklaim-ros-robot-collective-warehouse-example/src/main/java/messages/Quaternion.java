package messages;

public class Quaternion {
    public double x;
    public double y;
    public double z;
    public double w;



    public Quaternion(){}

    public Quaternion(double x, double y, double z, double w) {
        this.x = x;
        this.y = y;
        this.z = z;
        this.w = w;

    }




}
